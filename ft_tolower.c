static int	ft_isupper(const int c)
{
	if (c > 64 && c < 91)
	{
		return (1);
	}
	return (0);
}

int	ft_tolower(int c)
{
	if (ft_isupper(c))
	{
		c += 32;
	}
	return (c);
}
